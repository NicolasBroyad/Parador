function validarEquiposOctavos1() {
    var combo = document.getElementById("equipo1_octavos1");
    var equipo1 = combo.options[combo.selectedIndex].text;
    var combo1 = document.getElementById("equipo2_octavos1");
    var equipo2 = combo1.options[combo1.selectedIndex].text;

    if (equipo1 == equipo2) {
        alert("ERROR. No se puede enfrentar un mismo equipo entre si.");
        return false;
    }
}

